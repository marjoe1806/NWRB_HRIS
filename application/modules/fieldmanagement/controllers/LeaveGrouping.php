	public function index() {
		// Helper::rolehook(ModuleRels::BUDGET_CLASSIFICATIONS_SUB_MENU);
		$listData = array();
		$viewData = array();
		$page = "viewLeaveGrouping";
		$listData['key'] = $page;
		$ret = new LeaveGroupingCollection();
		if($ret->hasRows()){
			$res = new ModelResponse($ret->getCode(), $ret->getMessage(),$ret->getData());
			$respo = json_decode($res);
		}
		else{
			$res = new ModelResponse($ret->getCode(), $ret->getMessage());
			$respo = json_decode($res);
		}
		$listData['list'] = $respo;
		$viewData['table'] = $this->load->view("helpers/leavegroupinglist",$listData,TRUE); 
		if (!$this->input->is_ajax_request()) {
			Helper::setTitle('Leave Grouping');
			Helper::setMenu('templates/menu_template');
			Helper::setView('leavegrouping',$viewData,FALSE);
			Helper::setTemplate('templates/master_template');
		}
		else{
			$result['key'] = $listData['key'];
			$result['table'] = $viewData['table'];
			echo json_encode($result);
		}
		Session::checksession();
	}
