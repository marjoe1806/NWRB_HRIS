	public function index() {
		Helper::rolehook(ModuleRels::SATELLITE_LOCATIONS_SUB_MENU);
		$listData = array();
		$viewData = array();
		$page = "viewSatelliteLocations";
		$listData['key'] = $page;
		if (!$this->input->is_ajax_request()) {
			Helper::setTitle('Satellite Locations');
			Helper::setMenu('templates/menu_template');
			Helper::setView('satellitelocations',$viewData,FALSE);
			Helper::setTemplate('templates/master_template');
		}
		else{
			$result['key'] = $listData['key'];
			$result['table'] = $viewData['table'];
			echo json_encode($result);
		}
		Session::checksession();
	}
	public function loadSatelliteLocationsForm(){
		$formData = array();
		$result = array();
		$result['key'] = 'loadSatelliteLocations';
		if (!$this->input->is_ajax_request()) {
		   show_404();
		}
		else
		{
			$formData['key'] = $result['key'];
			$result['form'] = $this->load->view('forms/loadsatellitelocations.php', $formData, TRUE);
		}
		echo json_encode($result);
	}
	public function addSatelliteLocations(){
		$result = array();
		$page = 'addSatelliteLocations';
		if (!$this->input->is_ajax_request()) {
		   show_404();
		}
		else
		{
			if($this->input->post() && $this->input->post() != null)
			{
				$post_data = array();
				foreach ($this->input->post() as $k => $v) {
					$post_data[$k] = $this->input->post($k,true);
				}
				$ret =  new SatelliteLocationsCollection();
				if($ret->addRows($post_data)) {
					$res = new ModelResponse($ret->getCode(), $ret->getMessage(), $ret->getData());
				} else {
					$res = new ModelResponse($ret->getCode(), $ret->getMessage());
				}
				$result = json_decode($res,true);
			}
			else
			{
				$res = new ModelResponse();
				$result = json_decode($res,true);
			}
			$result['key'] = $page;
		}
		echo json_encode($result);
	}
